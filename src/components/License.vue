<script>
import URI from 'urijs'


export default {
  name: 'License',

  data: () => ({
    name: {
      ru: 'Мое приложение',
      ua: '',
      by: '',
    },
    hideForm: false,
  }),

  computed: {
    nameUa() { return this.name.ua || this.name.ru },
    nameBy() { return this.name.by || this.name.ru },
    href() {
      const { name } = this
      return new URI(window.location.href)
        .query('')
        .addQuery('ru', name.ru)
        .addQuery('ua', name.ua)
        .addQuery('by', name.by)
        .toString()
    },
    hrefRu() {
      const { name } = this
      return new URI(window.location.href)
        .query('')
        .addQuery('ru', name.ru)
        .addQuery('form', 'hide')
        .toString()
    },
    hrefUa() {
      const { name } = this
      return new URI(window.location.href)
        .query('')
        .addQuery('ua', name.ua || name.ru)
        .addQuery('form', 'hide')
        .toString()
    },
    hrefBy() {
      const { name } = this
      return new URI(window.location.href)
        .query('')
        .addQuery('by', name.by || name.ru)
        .addQuery('form', 'hide')
        .toString()
    },
  },

  created() {
    this.loadQ()
  },

  watch: {
    name: {
      handler: 'storeQ',
      deep: true,
    },
  },

  methods: {
    loadQ() {
      const q = new URLSearchParams(window.location.search)
      if (q.get('ru')) {
        this.name.ru = q.get('ru')
      }
      if (q.get('ua')) {
        this.name.ua = q.get('ua')
      }
      if (q.get('by')) {
        this.name.by = q.get('by')
      }
      if (q.get('form') === 'hide') {
        this.hideForm = true
      }
    },
    storeQ() {
      window.history.replaceState({}, document.title, this.href)
    },
  },
}
</script>

